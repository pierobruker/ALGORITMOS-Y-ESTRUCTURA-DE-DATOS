package listlinked;

public class Node<T> {
    private T data;  // Datos almacenados en el nodo
    private Node<T> next;  // Siguiente nodo

    public Node(T data) {
        this.data = data;
        this.next = null;
    }

    public T getData() {
        return data;
    }

    public void setData(T data) {
        this.data = data;
    }

    public Node<T> getNext() {
        return next;
    }

    public void setNext(Node<T> next) {
        this.next = next;
    }
}